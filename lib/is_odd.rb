# Public: Takes an integer and decides if its odd or not
#
# number - The integer that is decided if its odd or not
#
# Examples 
#
#     is_even(3)
#     # => True
#
#     is_even(4)
#     # => False
# Returns True if the integer is odd
# Returns False if the integer is even
def is_odd(number)
    if number % 2 == 0
        return false
    end
    return true
end

