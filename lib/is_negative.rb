# Public: Takes an integer and decides if it's negative or not
#
# number - The integer that is decided if it's negative or not
#
# Examples
#
#     is_negative(-1)
#     # => True
#
#     is_negative(1)
#     # => False

# Returns True if the integer is negative
# Returns False if the integer is not negative
def is_negative(number)
    if number < 0 
        return true
    end
    return false
end
