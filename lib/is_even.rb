# Public: Takes an integer and decides if its even or not
#
# number - The integer that is decided if its even or not
#
# Examples 
#
#     is_even(4)
#     # => True
#
#     is_even(3)
#     # => False
# Returns True if the integer is even
# Returns False if the integer is odd
def is_even(number)
    if number % 2 == 0
        return true
    end
    return false 
end

